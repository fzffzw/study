  <h1>老黑</h1>
  <p> 关于老黑的记忆太遥远，退缩到记忆的角落里，要深深挖掘才可以。 </p>
  <p>那还是我们住在平房的时候，房前有一片宽阔的空地，夏天的日子，空地上是向日葵，还有绿油油的葡萄架，葡萄架下有一个石头桌子，还有我和表哥玩弹球挖的小坑。 </p>
  <p>房子的左边有一颗粗粗的大树，树的品种记不太清楚了，只记得和平日总喜欢爬到树上去玩。那时候，北京城还没有那么大，我家已经算比较偏僻的地方了，过了不远的护城河就是一片片的菜地。而现在那里已经高楼林立，昼夜车水马龙。 </p>
  <p>老黑的年纪比我大，反正从我有记忆时起，就已经有老黑。老黑也没什么正式的名字，只因为她是黑色的，所以我就这样叫她。 </p>
  <p>那时候奶奶还在，老黑是奶奶的猫。</p>
  <p>七几年的时候，家里很困难，所以老黑平时也难得有什么好东西吃，平时都是自己去打猎，偶尔我会偷偷把肉“不小心”掉在她的身边，她便飞快地吃掉，似乎也怕被家里人看见了责怪。老黑也没有窝，晚上她是不许进屋的，只有睡在外面放杂物的地方。</p>
  <p>老黑怕我爸，这似乎是我家养过的猫的特征。有一天，姑姑和表姐们来家里玩，她也高兴地在屋子里撒欢，轰也轰不出去，结果我爸下班回来，指着门外冲老黑道：“出去！”老黑立刻飞也似地跑掉了。</p>
  <p>老黑是个捕猎高手，她总是蹲在墙头上，一动不动地，有大意的麻雀落在她身旁，便立刻一掌打过去，很少有落空的，这时候，她就有了改善生活的美餐。</p>
  <p>老黑真的很老了，有一次家里人发现她把生下来的小猫给吃掉，这表示她的年纪太大了，所以家里人决定把她扔掉。邻居家的哥哥骑车去扔猫，结果扔的人还没回来，猫已经到家了。第二次，大家说，猫怕水，就把她扔到护城河那边吧。但是过了两天，老黑又找回来了，于是大家再也不提扔她的事了。</p>
  <p>老黑却从没有嫌弃过这个家，一直就这么和我们在一起，直到我们搬家为止。我刚刚上学前班的时候，大院因为要盖楼，所以我们搬到了不远的临时周转房，但是搬家的时候老黑不在，我想等她回来的时候，看到空空的房子，会怎么想？她会不会觉得自己被人遗弃了呢？</p>
  <p>自那以后，我再也没见过老黑。</p>
  <p>写到这里，我有些写不下去了，因为搬进楼房不久，奶奶便去世了。后来听姑姑说，奶奶走的那天，她曾见到过一只黑猫在墙上叫。那是老黑么？我生命中的第一只精灵。</p>
  <p>关于老黑的故事，大多是从家里的长辈那里听来的，但是我能感觉到老黑就生活在这些故事里面。</p>
